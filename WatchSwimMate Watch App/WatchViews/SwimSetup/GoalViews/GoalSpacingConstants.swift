// GoalSpacingConstants.swift

import Foundation

// Shared spacing constants for consistent UI across SwimMate goal views
struct GoalSpacingConstants
{
    // MARK: - VStack Spacing
    // Main container VStack spacing (default: 8)
    static let mainContainer: CGFloat = 8

    // Large container spacing for major sections (16)
    static let largeContainer: CGFloat = 16

    // Small content spacing within components (4)
    static let smallContent: CGFloat = 4

    // Standard content spacing between related elements (8)
    static let standardContent: CGFloat = 8

    // MARK: - Padding Values
    // Horizontal padding for main container
    static let horizontalMain: CGFloat = 8

    // Large horizontal padding for wider spacing
    static let horizontalLarge: CGFloat = 16

    // Top padding for sections
    static let topSection: CGFloat = 8

    // Small top padding for minor elements
    static let topSmall: CGFloat = 2

    // Bottom padding for action buttons
    static let bottomAction: CGFloat = 10

    // Large bottom padding for containers
    static let bottomLarge: CGFloat = 16

    // MARK: - Grid and Layout Spacing
    // Spacing between grid items
    static let gridItem: CGFloat = 8

    // Spacing for preset button layouts
    static let presetButtons: CGFloat = 8
}